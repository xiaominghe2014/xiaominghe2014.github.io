
const _ = -1; // no map
const f = 0;  // floor
const p = 1;  // player
const w = 1 << 1; // wall
const x = 1 << 2; // box
const o = 1 << 3; // target


const char_player = '@';
const char_player_goal = '+';
const char_box = '$';
const char_box_goal = '*';
const char_wall = '#';
const char_goal = '.';
const char_floor = '-';
const char_no = '_';

function XSB2Map(xsbArr) {
    let map = [];
    for (let str of xsbArr) {
        let arr = [];
        for (let i = 0; i < str.length; i++) {
            let c = str[i];
            if (c === char_player) {
                arr.push(p);
            }
            if (c === char_player_goal) {
                arr.push(p | o);
            }
            if (c === char_box) {
                arr.push(x);
            }
            if (c === char_box_goal) {
                arr.push(x | o);
            }
            if (c === char_wall) {
                arr.push(w);
            }
            if (c === char_goal) {
                arr.push(o);
            }
            if (c === char_floor) {
                arr.push(f);
            }
            if (c === char_no || c == ' ') {
                arr.push(_);
            }
        }
        if (arr.length > 0) {
            map.push(arr);
        }
    }
    return map;
}

